    <script>
        function geofenceManager() {
            return {
                geofences: {!! json_encode($geofences->map(function($g) {
                    return [
                        'id' => $g->id,
                        'name' => $g->name,
                        'description' => $g->description,
                        'latitude' => $g->latitude,
                        'longitude' => $g->longitude,
                        'radius' => $g->radius,
                        'color' => $g->color,
                        'is_active' => $g->is_active,
                        'events_today' => $g->events->count(),
                    ];
                })->toArray()) !!},
                search: '',
                showDelete: false,
                deleteGeofence: {},

                get filteredGeofences() {
                    if (!this.search) return this.geofences;
                    
                    return this.geofences.filter(g => 
                        g.name.toLowerCase().includes(this.search.toLowerCase()) ||
                        (g.description && g.description.toLowerCase().includes(this.search.toLowerCase()))
                    );
                }
            }
        }
    </script>

